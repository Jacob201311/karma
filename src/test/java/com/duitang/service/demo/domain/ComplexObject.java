package com.duitang.service.demo.domain;

public class ComplexObject {

	protected int a;
	protected float b;
	protected long c;
	protected double d;
	protected boolean e;
	protected byte[] f;
	protected SimpleObject g;

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public float getB() {
		return b;
	}

	public void setB(float b) {
		this.b = b;
	}

	public long getC() {
		return c;
	}

	public void setC(long c) {
		this.c = c;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}

	public boolean isE() {
		return e;
	}

	public void setE(boolean e) {
		this.e = e;
	}

	public byte[] getF() {
		return f;
	}

	public void setF(byte[] f) {
		this.f = f;
	}

	public SimpleObject getG() {
		return g;
	}

	public void setG(SimpleObject g) {
		this.g = g;
	}

}
